#calculate simple interest
principle_amount = float(input("Enter principle amount: ")) #taking principle amount

time = float(input("Enter time in years: "))      # Taking time in year

rate_of_interest = float(input("Enter the rate of interest: ")) # taking rate of interst

Simple_Interest = (principle_amount*time*rate_of_interest)/100   #calculation

print("Simple interest is:",Simple_Interest) #printing SI